CREATE TABLE IF NOT EXISTS USER(
	USER_ID INT NOT NULL AUTO_INCREMENT,
	PRIMARY KEY(USER_ID),
	UNAME VARCHAR (25) NOT NULL UNIQUE,
	F_NAME VARCHAR(10) NULL,
	L_NAME VARCHAR(10) NULL,
	EMAIL VARCHAR(30) NOT NULL UNIQUE,
	PASSWORDHASH VARCHAR(50) NOT NULL,
	ROLE INT DEFAULT 2, -- <-  1 - ADMIN ABSOULTE POWER 2 - SEE PERMISSION TABLE FOR AUTHORIZATION
	BIO VARCHAR(140) NULL,
	PIC_PATH VARCHAR(10) NULL
);
CREATE TABLE IF NOT EXISTS BLOG (
	OWNER_ID INT  NOT NULL,
	BLOG_ID INT NOT NULL AUTO_INCREMENT,
	TITLE VARCHAR(225) NOT NULL,
	PRIMARY KEY(BLOG_ID),
	DATE_CREATED TIMESTAMP DEFAULT CURRENT_TIMESTAMP	
);
CREATE TABLE IF NOT EXISTS BLOG_POST (
	BLOG_ID INT UNIQUE NOT NULL,
	POST_ID INT NOT NULL AUTO_INCREMENT,
	PRIMARY KEY(POST_ID),
	TITLE VARCHAR(225) NOT NULL,
	CONTENT VARCHAR(10000) NOT NULL,
	DATE_CREATED TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
	LIKES INT DEFAULT 0,
	DISLIKES INT DEFAULT 0
);
CREATE TABLE IF NOT EXISTS PESMISSIONS (
	USER_ID INT NOT NULL,
	BLOG_ID INT NOT NULL
);